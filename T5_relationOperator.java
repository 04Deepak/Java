package java_basic_l1;

public class T5_relationOperator {

	public static void main(String[] args) {

		int a =2;
		int b =2;
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a!=b);
		System.out.println(a==b);
		
	}

}
